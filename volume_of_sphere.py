def calculate_volume_of_sphere(radius):
    pi = 3.14
    voume = ((4/3) * pi * (radius**3))
    return voume

radius1 = 10
v1 = calculate_volume_of_sphere(radius1)
print(f"The volume of the sphere {radius1} is: {v1}")

radius2 = 40
v2 = calculate_volume_of_sphere(radius2)
print(f"The volume of the sphere {radius1} is: {v2}")



